## How to access other project/locahost on browser using lan / command

# Check ip from Main system device using cmd
Cmd : ip a (main system - whose you want to access)

# ping from system in which you want to show hosted project / localhost 

CMD - ping (ip of server )

# Now Change hosts file from the system
Go to : C:\Windows\System32\drivers\etc\hosts

Change in file : add ip & link of project url/uri
Like : 

192.168.29.172        magento243.com
